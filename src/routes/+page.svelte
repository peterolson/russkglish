	<div>
		<p class="en">
			Note that not all of these sounds are easy for English and Russian speakers to pronounce. The goal is for English and
			Russian speakers to be able to <em>understand</em> Russkglish easily, not necessarily to be able to pronounce it easily. For
			example, the /ɵ/ vowel does not exist in either Russian or English. However, /sɵn/ will sound close enough to "son" for an
			English speaker to understand, and close enough to "сын" for a Russian speaker to understand. Neither English speakers nor
			Russian speakers are likely to be comfortable pronouncing /sɵn/.
		</p>
		<p class="ru">
			Обратите внимание, что не все эти звуки легко произносятся носителями английского и русского. Цель состоит в том, чтобы
			англо- и русскоязычные люди могли легко <em>понимать</em> русскглиш, а не обязательно легко произносить его. Например, гласный
			/ɵ/ не существует ни в русском, ни в английском языках. Однако /sɵn/ будет звучать достаточно близко к «son», чтобы англоговорящие
			поняли, и достаточно близко к «сын», чтобы русскоязычные поняли. Ни англоязычным, ни русскоязычным будет удобно произносить
			/sɵn/.
