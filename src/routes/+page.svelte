		<h3 class="en">Stress</h3>
		<h3 class="ru">Ударение</h3>
	</div>
	<div>
		<p class="en">
			Syllable stress in Russkglish is always on the first syllable unless indicated otherwise by an accent mark. For example,
			the first syllable of
			<em>problem/проблем</em> is stressed, while the second syllable of <em>apártament/апа́ртамент</em> is stressed.
		</p>
		<p class="ru">
			Ударение в русскглише всегда на первой слоге, если не указано иначе знаком ударения. Например, первый слог в
			<em>проблем/problem</em> ударен, а в <em>апа́ртамент/apártament</em> ударен второй слог.
		</p>
	</div>
	<div>
		<h2 class="en">Grammar</h2>
		<h2 class="ru">Грамматика</h2>
	</div>
	<div>
		<p class="en">
			Even though Russian and English both have some degree of inflectional morphology, they are not compatible enough to merge
			together in a comprehensible way. As such, Russkglish is an analytic language, denoting grammatical relationships via
			function words and word order, rather than prefixes or suffixes. For example, verb tense and aspect can be conveyed
			through helper words like <em>start/старт</em> and <em>finish/финиш</em>.
		</p>
		<p class="ru">
			Несмотря на то, что русский и английский языки имеют некоторую степень флективной морфологии, они недостаточно совместимы,
			чтобы слиться вместе понятным образом. Поэтому, русскглиш является аналитическим языком, обозначающим грамматические
			отношения с помощью служебных слов и порядка слов, а не префиксов или суффиксов. Например, время и вид глагола можно
			передать с помощью вспомогательных слов, таких как <em>старт/start</em> и <em>финиш/finish</em>.
		</p>
