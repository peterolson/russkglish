		<h1 class="en">Russkglish</h1>
		<h1 class="ru">Русскглиш</h1>
	</div>
	<div>
		<p class="en">
			Russkglish is a language designed to be a bridge of comprehensibility between English and Russian. That is, English
			speakers with no knowledge of Russian and Russian speakers with no knowledge of English should both be able to understand
			Russkglish without much effort.
		</p>
		<p class="ru">
			Русскглиш — это язык, создан для того, чтобы стать мостом между английским языком и русским языком. То есть,
			английскоязычные, которые не знают русский, и русскоязычные, которые не знают английский, одинакого могут понять русскглиш
			без особых усилий.
		</p>
	</div>
	<div>
		<h2 class="en">Vocabulary</h2>
		<h2 class="ru">Лексика</h2>
	</div>
	<div>
		<p class="en">For English speakers and Russian speakers to both be able to understand, words must be chosen carefully.</p>
		<p class="ru">Чтобы английскоязычные и русскоязычные одинаково могли понять, требуется внимательно выбирать слова.</p>
	</div>
	<div>
		<h3 class="en">Direct cognates</h3>
		<h3 class="ru">Прямые родственники</h3>
	</div>
	<div>
		<p class="en">
			Fortunately, there are already many words that are almost the same in both languages. These similarities can come about in
			a number of ways:
		</p>

		<p class="ru">К счастью, многие слова уже почти одинаковы в обоих языках. Эти сходства появляются с разных источников:</p>
	</div>
	<div>
		<ul class="en">
			<li>
				<strong>Common Indo-European ancestry</strong> is the reason for similarities in very basic words
				<em>brother/брат</em>, <em>sister/сестра</em>, and <em>three/три</em>.
			</li>
			<li>
				Vocaubulary in specialized domains often come from <strong>Latin and Greek roots</strong> in both languages, such as
				<em>administration/администрация</em>,
				<em>automobile/автомобиль</em>
				and
				<em>arachnophobia/арахнофобия</em>.
			</li>
			<li>
				<strong>French</strong> was a prestige language in both England and Russia for many years, so both languages have many
				words from French, such as <em>chauffeur/шофер</em>,
				<em>chief/шеф</em>, and <em>balcony/балкон</em>.
			</li>
			<li>
				Many modern-day words in Russian are <strong>anglicisms</strong>, such as
				<em>popcorn/попкорн</em>, <em>internet/интернет</em>, and <em>smartphone/смартфон</em>.
			</li>
		</ul>
		<ul class="ru">
			<li>
				<strong>Общее индоевропейское происхождение</strong> — это причина сходства в очень простых словах как
				<em>брат/brother</em>, <em>сестра/sister</em> и <em>три/three</em>.
			</li>
			<li>
				Слова в специализированных областях часто имеют <strong>латинские и греческие корни</strong>
				в обоих языках, в том числе <em>администрация/administration</em>,
				<em>автомобиль/automobile</em>
				и
				<em>арахнофобия/arachnophobia</em>.
			</li>
			<li>
				<strong>Французский</strong> был престижным языком в Англии и России в течение многих лет, поэтому оба языка получили
				много слов из французского, таких как <em>шофер/chauffeur</em>,
				<em>шеф/chief</em> и <em>балкон/balcony</em>.
			</li>
			<li>
				Многие современные слова в русском языке являются <strong>англицизмами</strong>, такими как
				<em>попкорн/popcorn</em>, <em>интернет/internet</em> и <em>смартфон/smartphone</em>.
			</li>
		</ul>
	</div>
	<div>
		<h3 class="en">Creativity</h3>
		<h3 class="ru">Креативность</h3>
	</div>
	<div>
		<p class="en">
			For words that are not direct cognates, there are a number of creative tricks one can use to find similarities.
		</p>
		<p class="ru">Для слов, которые не являются прямыми родственниками, можно найти сходства более креативным образом.</p>
	</div>
	<div>
		<ul class="en">
			<li>
				<strong>Semantically similar words</strong> are very helpful. For example, there is no similar-sounding word in English
				and Russian for "street", but <em>asphalt/асфальт</em> is a common material for building streets and is semantically close
				enough to "street" to be understood in context.
			</li>
			<li>
				<strong>Onomatopoeia</strong> can sometimes bridge some gaps. For example, there is no synonym of "drink" that sounds
				similar in English and Russian, but
				<em>glug glug/глотать</em> has a sound that intuitively represents the sound of swallowing liquid.
			</li>
		</ul>
		<ul class="ru">
			<li>
				<strong>Семантически похожие слова</strong> очень полезны. Например, в английском и русском языках нет похожих по
				звучанию слов для "улицы", но <em>асфальт/asphalt</em>, материал для постройки улиц, достаточно близок к "улице", чтобы
				быть понятым в контексте.
			</li>
			<li>
				<strong>Звукоподражание</strong> иногда может помочь. Например, нет синонима для "пить", который звучит похоже на
				английском и на русском, но <em>глотать/glug glug</em> интуитивно представляет звук глотания жидкости.
			</li>
		</ul>
	</div>
